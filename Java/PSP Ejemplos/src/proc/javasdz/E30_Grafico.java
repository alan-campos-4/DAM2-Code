    /**
     * Evento pulsación botón
     *
     * @param evt
     */
    private void jButtonEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEjecutarActionPerformed

        String programa = this.jTextFieldPrograma.getText().trim();
        if (programa.equals("")) {
            JOptionPane.showMessageDialog(this, "Introduzca el nombre del programa");
            return;
        }

        ProcessBuilder pb;
        try {
            System.out.println("\nArrancando programa: " + programa);
            pb = new ProcessBuilder(programa);

            // Indicamos que arrancamos el proceso. Aun no se ha arrancado
            Process proceso = pb.start();

            // LLevamos registro de los procesos creados
            this.listaProcesos.add(proceso);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Hemos tenido un contratiempo. Mire la consola.");
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButtonEjecutarActionPerformed

    /**
     * Cerramos aplicación y procesos hijos
     *
     * @param evt
     */
    private void jButtonSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalirActionPerformed
        // Matamos los procesos hijos
        for (Process proc : this.listaProcesos) {
            proc.destroy();
        }
        // Finalizamos aplicación
        System.exit(0);
    }//GEN-LAST:event_jButtonSalirActionPerformed

    /**
     * Permite seleccionar el programa con una ventna
     * @param evt 
     */
    private void jButtonFileDialogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFileDialogActionPerformed

        FileDialog fd = new FileDialog(this, "Seleccione una aplicación", FileDialog.LOAD);
        fd.setDirectory("C:\\");
        fd.setFile("*.exe");
        fd.setVisible(true);
        String filename = fd.getDirectory() + fd.getFile();
        this.jTextFieldPrograma.setText(filename);
        if (filename == null) {
            System.out.println("You cancelled the choice");
        } else {
            System.out.println("You chose " + filename);
        }


    }//GEN-LAST:event_jButtonFileDialogActionPerformed

